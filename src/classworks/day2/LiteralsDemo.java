package classworks.day2;

public class LiteralsDemo {

    public static void main(String[] args) {
        //Literals: a source code representation of fixed value
        //literals -> fixed value + data type

        //1. Integer Literal - long value L
        int i = 2121;
        long l = 34212L;

        //2. Floating Literal - floating value F
        float f = 232.324F;
        double d = 32343.1231;

        //3. Boolean Literal

        //4. Character Literal - single quote
        char c1 = '1';
        char c2 = 'a';
        char c3 = '?';
        char c4 = '\u0000';

        //5. String Literal

        //6. Null Literal
        String s = null;
        LiteralsDemo ob = new LiteralsDemo(); //this is an object
        LiteralsDemo obj = null;
    }
}
